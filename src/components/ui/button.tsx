import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center gap-2 whitespace-nowrap rounded-lg text-sm font-semibold transition-all duration-300 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-portfolio-orange focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 [&_svg]:pointer-events-none [&_svg]:size-4 [&_svg]:shrink-0",
  {
    variants: {
      variant: {
        default: "bg-portfolio-orange text-portfolio-navy hover:bg-portfolio-orange-light hover:scale-105 shadow-lg hover:shadow-portfolio-hover",
        secondary: "bg-transparent text-portfolio-white border-2 border-portfolio-white hover:bg-portfolio-white hover:text-portfolio-navy",
        outline: "border-2 border-portfolio-orange text-portfolio-orange hover:bg-portfolio-orange hover:text-portfolio-navy",
        ghost: "text-portfolio-gray hover:text-portfolio-white hover:bg-portfolio-navy-light",
        link: "text-portfolio-orange underline-offset-4 hover:underline hover:text-portfolio-orange-light",
        cta: "bg-gradient-cta text-portfolio-navy hover:scale-105 shadow-xl hover:shadow-portfolio-hover",
      },
      size: {
        default: "h-12 px-6 py-3",
        sm: "h-9 px-4 py-2 text-xs",
        lg: "h-14 px-8 py-4 text-base",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }
